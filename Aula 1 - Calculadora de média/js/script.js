var nome = "Guilherme";
var notaDoPrimeiroBimestre = 9;
var notaDoSegundoBimestre = 3.2;
var notaDoTerceiroBimestre = 7;
var notaDoQuartoBimestre = 7;

var somaDasNotas =
  notaDoPrimeiroBimestre +
  notaDoSegundoBimestre +
  notaDoTerceiroBimestre +
  notaDoQuartoBimestre;

var notaFinal = somaDasNotas / 4;

var notaFixada = notaFinal.toFixed(1);

console.log(`Bem vindo, ${nome}! Você teve uma nota final de: ${notaFixada}`);

